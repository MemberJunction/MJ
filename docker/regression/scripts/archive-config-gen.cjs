/**
 * Archive config generator — builds a transient `mj.config.cjs` from
 * ARCHIVE_DB_* env vars and writes it to a temp path so the push command
 * can target it via MJ_CONFIG_FILE.
 *
 * The archive flow is fully env-var driven — users just set ARCHIVE_DB_*;
 * there is no config file to copy or edit.
 *
 * Defaults applied here (any can be overridden via env):
 *   ARCHIVE_DB_HOST          host.docker.internal
 *   ARCHIVE_DB_PORT          1433
 *   ARCHIVE_DB_TRUST_CERT    true
 *   ARCHIVE_USER_EMAIL       not.set@nowhere.com
 *   ARCHIVE_MJ_CORE_SCHEMA   __mj
 *   MJ_BASE_ENCRYPTION_KEY   (passed through; must match destination)
 *
 * Output: writes the config to a file (path passed as argv[2], defaults to
 * /tmp/mj-archive-config.cjs) and prints that path to stdout. The shell
 * captures stdout to feed MJ_CONFIG_FILE.
 *
 * Usage:
 *   CONFIG_PATH=$(node archive-config-gen.cjs)
 *   MJ_CONFIG_FILE="$CONFIG_PATH" npx mj sync push ...
 */

const fs = require('node:fs');

const outputPath = process.argv[2] || '/tmp/mj-archive-config.cjs';

const env = {
    dbHost: process.env.ARCHIVE_DB_HOST || 'host.docker.internal',
    dbPort: parseInt(process.env.ARCHIVE_DB_PORT || '1433', 10),
    dbDatabase: process.env.ARCHIVE_DB_DATABASE || '',
    dbUsername: process.env.ARCHIVE_DB_USERNAME || '',
    dbPassword: process.env.ARCHIVE_DB_PASSWORD || '',
    dbTrustCert: (process.env.ARCHIVE_DB_TRUST_CERT || 'true').toLowerCase() === 'true',
    userEmail: process.env.ARCHIVE_USER_EMAIL || 'not.set@nowhere.com',
    coreSchema: process.env.ARCHIVE_MJ_CORE_SCHEMA || '__mj',
    encryptionKey: process.env.MJ_BASE_ENCRYPTION_KEY || '',
};

if (!env.dbDatabase || !env.dbUsername || !env.dbPassword) {
    process.stderr.write(
        '✗ archive-config-gen: missing required ARCHIVE_DB_DATABASE / ARCHIVE_DB_USERNAME / ARCHIVE_DB_PASSWORD\n',
    );
    process.exit(1);
}

// JSON.stringify the values into the generated config so any special chars
// in the password (e.g. @ ! quotes) survive without manual escaping.
const config = `/**
 * Auto-generated by archive-config-gen.cjs at ${new Date().toISOString()}.
 * Sourced from ARCHIVE_DB_* env vars in the test-runner container.
 * Do not edit by hand — re-run the suite to regenerate.
 */
module.exports = {
    dbHost: ${JSON.stringify(env.dbHost)},
    dbPort: ${env.dbPort},
    dbDatabase: ${JSON.stringify(env.dbDatabase)},
    dbUsername: ${JSON.stringify(env.dbUsername)},
    dbPassword: ${JSON.stringify(env.dbPassword)},
    dbTrustServerCertificate: ${env.dbTrustCert ? 'true' : 'false'},
    currentUserEmail: ${JSON.stringify(env.userEmail)},
    mjCoreSchema: ${JSON.stringify(env.coreSchema)},
    baseEncryptionKey: ${JSON.stringify(env.encryptionKey)},
};
`;

fs.writeFileSync(outputPath, config, { mode: 0o600 });
process.stdout.write(outputPath);
