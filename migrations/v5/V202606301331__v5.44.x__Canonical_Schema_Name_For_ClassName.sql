CREATE OR ALTER VIEW [${flyway:defaultSchema}].[vwEntities]
AS
SELECT
    e.*,
    /* CodeName: Schema-prefixed programmatic name derived from entity Name.
       Uses GetClassNameSchemaPrefix(COALESCE(CanonicalSchemaName, SchemaName)) for the
       prefix, then strips the EntityNamePrefix from the Name (if present) and removes spaces. */
    [${flyway:defaultSchema}].GetProgrammaticName(
        [${flyway:defaultSchema}].GetClassNameSchemaPrefix(COALESCE(si.CanonicalSchemaName, e.SchemaName)) +
        REPLACE(
            IIF(si.EntityNamePrefix IS NOT NULL,
                REPLACE(e.Name, si.EntityNamePrefix, ''),
                e.Name
            ),
            ' ',
            ''
        )
    ) AS CodeName,
    /* ClassName: Schema-prefixed programmatic class name for TypeScript entity classes,
       Zod schemas, and Angular form components. Uses
       GetClassNameSchemaPrefix(COALESCE(CanonicalSchemaName, SchemaName)) so that the
       canonical (case-stable) schema name drives the prefix when present. This keeps the
       class name aligned with the GraphQL type name and the published entity packages. */
    [${flyway:defaultSchema}].GetProgrammaticName(
        [${flyway:defaultSchema}].GetClassNameSchemaPrefix(COALESCE(si.CanonicalSchemaName, e.SchemaName)) +
        e.BaseTable +
        ISNULL(e.NameSuffix, '')
    ) AS ClassName,
    [${flyway:defaultSchema}].GetProgrammaticName(e.BaseTable + ISNULL(e.NameSuffix, '')) AS BaseTableCodeName,
    par.Name ParentEntity,
    par.BaseTable ParentBaseTable,
    par.BaseView ParentBaseView,
    si.CanonicalSchemaName AS CanonicalSchemaName
FROM
    [${flyway:defaultSchema}].Entity e
LEFT OUTER JOIN
    [${flyway:defaultSchema}].Entity par
ON
    e.ParentID = par.ID
LEFT OUTER JOIN
    [${flyway:defaultSchema}].SchemaInfo si
ON
    e.SchemaName = si.SchemaName
GO

-- =============================================================================
-- STEP 3: Backfill CanonicalSchemaName from the installed-app record inside
--         spUpdateSchemaInfoFromDatabase (the CodeGen metadata-sync proc).
--
--   The OpenApp install records the canonical (case-preserved) schema name on
--   the OpenApp table's SchemaName column, but the SchemaInfo row is materialized HERE,
--   from the DB catalog (lowercase on PG) — so a fresh-install-then-codegen flow
--   would otherwise leave CanonicalSchemaName NULL on the new row. We backfill it
--   catalog-only (no manifest-file reading): for any SchemaInfo row still missing
--   a canonical name, copy it from a matching OpenApp record, joined CASE-
--   INSENSITIVELY (SchemaInfo.SchemaName is the physical/folded name; OpenApp.
--   SchemaName is canonical). Only fills NULLs — never overwrites a value the
--   install already set.
--
--   This proc is HAND-MAINTAINED in the baseline (CodeGen does NOT regenerate it
--   on SQL Server), so this CREATE OR ALTER persists. The PostgreSQL counterpart
--   is regenerated every codegen run from CodeGenLib's metadataSupportObjects.ts,
--   so the SAME backfill is added there too (keep them in sync).
--
--   Reproduces the current proc verbatim and appends ONLY the final backfill UPDATE.
-- =============================================================================
PRINT N'Updating spUpdateSchemaInfoFromDatabase to backfill CanonicalSchemaName'
GO

CREATE OR ALTER PROCEDURE [${flyway:defaultSchema}].[spUpdateSchemaInfoFromDatabase]
    @ExcludedSchemaNames NVARCHAR(MAX) = NULL
AS
BEGIN
    SET NOCOUNT ON;

    -- Parse excluded schema names into a table variable
    DECLARE @ExcludedSchemas TABLE (SchemaName NVARCHAR(128))

    IF @ExcludedSchemaNames IS NOT NULL AND LEN(@ExcludedSchemaNames) > 0
    BEGIN
        INSERT INTO @ExcludedSchemas (SchemaName)
        SELECT TRIM(value) FROM STRING_SPLIT(@ExcludedSchemaNames, ',')
        WHERE TRIM(value) <> ''
    END

    -- Update descriptions for existing SchemaInfo records from extended properties
    UPDATE si
    SET si.Description = ss.SchemaDescription
    FROM [${flyway:defaultSchema}].SchemaInfo si
    INNER JOIN [${flyway:defaultSchema}].vwSQLSchemas ss
        ON si.SchemaName = ss.SchemaName
    WHERE
        (si.Description IS NULL OR si.Description <> ISNULL(ss.SchemaDescription, ''))
        AND ss.SchemaName NOT IN (SELECT SchemaName FROM @ExcludedSchemas)

    -- Insert new SchemaInfo records for schemas that don't exist yet
    -- We'll use default entity ID ranges that can be updated later by administrators
    INSERT INTO [${flyway:defaultSchema}].SchemaInfo
    (
        SchemaName,
        EntityIDMin,
        EntityIDMax,
        Comments,
        Description
    )
    SELECT
        ss.SchemaName,
        1,  -- Default min ID, must be > 0 per CHECK constraint. Should be updated by administrator.
        999999999,  -- Default max ID, must be > 0 per CHECK constraint. Should be updated by administrator.
        'Auto-created by CodeGen. Please update EntityIDMin and EntityIDMax to appropriate values for this schema.',
        ss.SchemaDescription
    FROM
        [${flyway:defaultSchema}].vwSQLSchemas ss
    LEFT OUTER JOIN
        [${flyway:defaultSchema}].SchemaInfo si ON ss.SchemaName = si.SchemaName
    WHERE
        si.ID IS NULL  -- Schema doesn't exist in SchemaInfo yet
        AND ss.SchemaName NOT IN (SELECT SchemaName FROM @ExcludedSchemas)

    -- Backfill the case-stable canonical schema name from the installed Open App
    -- record. SchemaInfo.SchemaName is the physical (lowercased on PG) name; the app
    -- record carries the canonical casing. Case-insensitive join; only fills NULLs.
    UPDATE si
    SET si.CanonicalSchemaName = app.SchemaName
    FROM [${flyway:defaultSchema}].SchemaInfo si
    INNER JOIN [${flyway:defaultSchema}].OpenApp app
        ON LOWER(si.SchemaName) = LOWER(app.SchemaName)
    WHERE si.CanonicalSchemaName IS NULL
      AND app.SchemaName IS NOT NULL

    -- Return the updated/inserted records
    SELECT
        si.*
    FROM
        [${flyway:defaultSchema}].SchemaInfo si
    INNER JOIN
        [${flyway:defaultSchema}].vwSQLSchemas ss ON si.SchemaName = ss.SchemaName
    WHERE
        ss.SchemaName NOT IN (SELECT SchemaName FROM @ExcludedSchemas)
END
GO











































































