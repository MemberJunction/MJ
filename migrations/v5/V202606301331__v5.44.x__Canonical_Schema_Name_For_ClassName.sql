-- =============================================================================
-- Migration: Canonical Schema Name for Entity ClassName / CodeName
-- Version:   v5.44.x
-- Purpose:   Make entity ClassName/CodeName (and the runtime GraphQL type name)
--            derive from a CASE-STABLE canonical schema name, so PostgreSQL
--            installs — where unquoted DDL folds the physical schema to
--            lowercase — still produce PascalCase prefixes that match the
--            published, hand-cased entity packages (e.g. @mj-biz-apps/common-entities
--            exports mjBizAppsCommon...Entity).
--
-- Background:
--   GetClassNameSchemaPrefix(SchemaName) preserves the input case verbatim
--   (the core MJ schema is special-cased to the 'MJ' prefix). On SQL Server the
--   schema is stored as authored (__mj_BizAppsCommon -> 'mjBizAppsCommon'). On PostgreSQL the
--   physical schema is folded to lowercase (__mj_bizappscommon ->
--   'mjbizappscommon'), which no longer matches the published class names ->
--   consuming-app builds fail with TS2724.
--
--   The lowercase physical schema is correct/required on PG (it is what
--   SQLDialect.CanonicalSchemaName + the runtime QuoteSchema resolve to). The
--   canonical casing survives only in the app manifest (mj-app.json schema.name),
--   so we persist it on SchemaInfo and prefer it (with a COALESCE fallback to
--   SchemaName) when computing the prefix.
--
-- Changes:
--   1. SchemaInfo gets a new nullable CanonicalSchemaName column. NULL on every
--      existing row / install, on the core MJ schema, and on SQL Server (where
--      SchemaName is already canonical) -> COALESCE falls back to SchemaName ->
--      NET-ZERO change to ClassName/CodeName there.
--   2. vwEntities (a CUSTOM core view, BaseViewGenerated = 0, hand-maintained in
--      migrations and NOT regenerated by CodeGen — same as the original
--      V202602190836 prefix migration that edited it) is rewritten to feed
--      COALESCE(si.CanonicalSchemaName, e.SchemaName) into the two
--      GetClassNameSchemaPrefix calls (CodeName + ClassName), and to expose the
--      new column as a virtual EntityInfo field.
--
-- The GetClassNameSchemaPrefix and GetProgrammaticName functions are UNCHANGED —
-- only the input fed to the prefix function changes.
--
-- NOTE on migrations/CLAUDE.md "no view edits" rule: that rule covers entity
-- BASE views that CodeGen owns. vwEntities is a hand-maintained system view that
-- CodeGen does not regenerate, so editing it here is correct and precedented.
--
-- Net effect on existing SQL Server / core-schema entities: NONE (CanonicalSchemaName
-- is NULL -> COALESCE -> SchemaName, identical to today).
-- =============================================================================

