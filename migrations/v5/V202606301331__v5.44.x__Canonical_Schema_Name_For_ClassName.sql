-- STEP 2: Recreate vwEntities preferring CanonicalSchemaName for the prefix.
--         Reproduces the current view verbatim, changing ONLY:
--           - GetClassNameSchemaPrefix(e.SchemaName)
--               -> GetClassNameSchemaPrefix(COALESCE(si.CanonicalSchemaName, e.SchemaName))
--             in BOTH the CodeName and ClassName expressions, AND
--           - adds si.CanonicalSchemaName AS CanonicalSchemaName to the SELECT
--             (so EntityInfo.CanonicalSchemaName populates).
--         BaseTableCodeName has no schema prefix and is left untouched.
