-- ============================================================================
-- Re-apply the TS-identifier-quoting cleanup as the newest migration, so it runs
-- on BOTH fresh installs (after the v5.38 baseline floor) and existing installs
-- already deployed up to v5.41/v5.42 (in-order, as the latest version).
--
-- Background:
--   V202605041700__v5.33.x__Fix_TS_Identifier_Quoting_In_Code_Columns.pg-only.sql
--   stripped converter-introduced double-quoting from non-SQL code columns
--   (turning  params."Params".find(p => p."Name" ...)  back into
--   params.Params.find(p => p.Name ...)). That fix shipped at v5.33, AFTER the
--   then-current floor (the v5.0 baseline), so it cleaned the data.
--
--   Since then, newer PostgreSQL baselines were regenerated by the same
--   SQL Server -> PostgreSQL converter, which STILL emits the double-quoting bug.
--   The current floor baseline — B202605291452__v5.38.x__Baseline.pg.sql — once
--   again contains  XYZ."FieldName"  quoting inside __mj.Action.Code and
--   __mj.TemplateContent.TemplateText. On a fresh install Skyway starts from the
--   v5.38 baseline, so the v5.33 fix (now BELOW the floor) never runs and the
--   corruption survives. CodeGen then writes invalid TypeScript into CoreActions
--   (e.g.  const name = params."Params".find(...) ) and the downstream build +
--   class-registration manifest fail, breaking `mj codegen` and `mj sync push`
--   on every fresh PostgreSQL build.
--
-- Why this file is dated as the newest migration (NOT back-dated to v5.38):
--   A forward-fix must apply to existing databases too. Databases already at
--   v5.41/v5.42 would treat a v5.38-era version as out-of-order and (with
--   Skyway/Flyway outOfOrder=false) skip it entirely. Dating it after the latest
--   migration makes it apply in-order everywhere. Placement does not affect fresh
--   installs: CodeGen runs after the ENTIRE migrate pass, and no migration reads
--   these code columns' quoting, so cleaning them last is sufficient. This mirrors
--   the original v5.33 fix, which was tagged with the then-current release rather
--   than the baseline it repaired.
--
-- Fix:
--   Re-run the identical, idempotent strip. The PG regex
--   `\.\"([A-Za-z_][A-Za-z0-9_]*)\"` only matches `."Name"` AFTER a dot (JS/Nunjucks
--   property-access shape), never a standalone SQL identifier, and is a no-op on
--   already-clean data.
--
-- Scope:
--   PG-only forward fix (SQL Server data is unaffected — its case-insensitive
--   collation never triggered the converter quoting). The B-baseline file remains
--   unchanged per project policy (Rule 1). The converter-side root cause
--   (preventing recurrence on the next baseline regen) remains tracked as a
--   separate issue; this migration must be re-issued as the newest version after
--   each new baseline regen until the converter is fixed.
-- ============================================================================

UPDATE ${flyway:defaultSchema}."Action"
SET "Code" = regexp_replace("Code", '\.\"([A-Za-z_][A-Za-z0-9_]*)\"', '.\1', 'g')
WHERE "Code" ~ '\.\"[A-Za-z_]';

UPDATE ${flyway:defaultSchema}."Action"
SET "CodeComments" = regexp_replace("CodeComments", '\.\"([A-Za-z_][A-Za-z0-9_]*)\"', '.\1', 'g')
WHERE "CodeComments" ~ '\.\"[A-Za-z_]';

UPDATE ${flyway:defaultSchema}."TemplateContent"
SET "TemplateText" = regexp_replace("TemplateText", '\.\"([A-Za-z_][A-Za-z0-9_]*)\"', '.\1', 'g')
WHERE "TemplateText" ~ '\.\"[A-Za-z_]';

DO $$
DECLARE
    action_count INTEGER;
    template_count INTEGER;
BEGIN
    SELECT count(*) INTO action_count FROM ${flyway:defaultSchema}."Action" WHERE "Code" ~ '\.\"[A-Za-z_]';
    SELECT count(*) INTO template_count FROM ${flyway:defaultSchema}."TemplateContent" WHERE "TemplateText" ~ '\.\"[A-Za-z_]';

    IF action_count > 0 OR template_count > 0 THEN
        RAISE WARNING 'TS-quoting cleanup left residual: % Action.Code rows, % TemplateContent.TemplateText rows still match', action_count, template_count;
    END IF;
END $$;
