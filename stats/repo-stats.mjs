#!/usr/bin/env node
/**
 * repo-stats.mjs — Lines-of-code snapshot generator for the /stats folder.
 *
 * Counts git-tracked lines by language using cloc, appends a row to
 * stats/data.csv, writes a full per-run report to stats/reports/, and
 * regenerates stats/README.md (history table + mermaid trend charts).
 *
 * Usage:
 *   node stats/repo-stats.mjs              # snapshot the current HEAD/working tree
 *   node stats/repo-stats.mjs <commit>     # snapshot a historical commit (via cloc --git)
 *   node stats/repo-stats.mjs <c1> <c2> …  # backfill several commits in one run
 *
 * Requires cloc (brew install cloc).
 */
import { execFileSync } from 'node:child_process';
import { mkdirSync, readFileSync, writeFileSync, existsSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const repoRoot = join(dirname(fileURLToPath(import.meta.url)), '..');
const statsDir = join(repoRoot, 'stats');
const reportsDir = join(statsDir, 'reports');
const csvPath = join(statsDir, 'data.csv');

// Language buckets tracked as columns in data.csv. Anything not listed rolls into "Other".
const LANGUAGE_GROUPS = {
    TypeScript: ['TypeScript'],
    JavaScript: ['JavaScript', 'JSX'],
    HTML: ['HTML'],
    CSS: ['CSS', 'SCSS', 'Sass', 'Less'],
    Markdown: ['Markdown'],
    SQL: ['SQL'],
    JSON: ['JSON', 'JSON5'],
};
// "Source" = hand-written application source for the headline trend (SQL/JSON tracked separately).
const SOURCE_GROUPS = ['TypeScript', 'JavaScript', 'HTML', 'CSS', 'Markdown'];
const CSV_COLUMNS = ['date', 'commit', ...Object.keys(LANGUAGE_GROUPS), 'Other', 'SourceTotal', 'GrandTotal', 'Files'];

function git(...args) {
    return execFileSync('git', args, { cwd: repoRoot, encoding: 'utf8' }).trim();
}

function runCloc(commitish) {
    const args = commitish
        ? ['--git', commitish, '--json', '--quiet']
        : ['--vcs=git', '--json', '--quiet'];
    const out = execFileSync('cloc', args, { cwd: repoRoot, encoding: 'utf8', maxBuffer: 64 * 1024 * 1024 });
    return JSON.parse(out);
}

function groupCounts(clocJson) {
    const groups = Object.fromEntries([...Object.keys(LANGUAGE_GROUPS), 'Other'].map((g) => [g, 0]));
    const byLanguage = [];
    for (const [lang, v] of Object.entries(clocJson)) {
        if (lang === 'header' || lang === 'SUM') continue;
        byLanguage.push({ lang, files: v.nFiles, blank: v.blank, comment: v.comment, code: v.code });
        const group = Object.keys(LANGUAGE_GROUPS).find((g) => LANGUAGE_GROUPS[g].includes(lang)) ?? 'Other';
        groups[group] += v.code;
    }
    byLanguage.sort((a, b) => b.code - a.code);
    const sourceTotal = SOURCE_GROUPS.reduce((sum, g) => sum + groups[g], 0);
    return {
        groups,
        byLanguage,
        sourceTotal,
        grandTotal: clocJson.SUM.code,
        files: clocJson.SUM.nFiles,
    };
}

function loadCsv() {
    if (!existsSync(csvPath)) return [];
    const [header, ...lines] = readFileSync(csvPath, 'utf8').trim().split('\n');
    const cols = header.split(',');
    return lines.filter(Boolean).map((line) => {
        const cells = line.split(',');
        return Object.fromEntries(cols.map((c, i) => [c, cells[i]]));
    });
}

function saveCsv(rows) {
    rows.sort((a, b) => a.date.localeCompare(b.date));
    const lines = [CSV_COLUMNS.join(',')];
    for (const row of rows) lines.push(CSV_COLUMNS.map((c) => row[c]).join(','));
    writeFileSync(csvPath, lines.join('\n') + '\n');
}

function upsertRow(rows, row) {
    const existing = rows.findIndex((r) => r.date === row.date);
    if (existing >= 0) rows[existing] = row;
    else rows.push(row);
}

function writeReport(date, sha, counts) {
    const fmt = (n) => n.toLocaleString('en-US');
    const lines = [
        `# Repo Stats — ${date}`,
        '',
        `- **Commit**: \`${sha}\``,
        `- **Source LOC** (TS + JS + HTML + CSS + MD): **${fmt(counts.sourceTotal)}**`,
        `- **Total LOC** (all tracked languages): **${fmt(counts.grandTotal)}** across ${fmt(counts.files)} files`,
        '',
        '| Language | Files | Blank | Comment | Code |',
        '|---|---:|---:|---:|---:|',
        ...counts.byLanguage.map((l) => `| ${l.lang} | ${fmt(l.files)} | ${fmt(l.blank)} | ${fmt(l.comment)} | ${fmt(l.code)} |`),
        `| **SUM** | **${fmt(counts.files)}** | | | **${fmt(counts.grandTotal)}** |`,
        '',
        `_Generated by \`stats/repo-stats.mjs\` using cloc (git-tracked files only)._`,
        '',
    ];
    writeFileSync(join(reportsDir, `${date}.md`), lines.join('\n'));
}

function mermaidChart(title, yLabel, dates, series) {
    const lines = [
        '```mermaid',
        'xychart-beta',
        `    title "${title}"`,
        `    x-axis [${dates.map((d) => `"${d}"`).join(', ')}]`,
        `    y-axis "${yLabel}"`,
    ];
    for (const values of series) lines.push(`    line [${values.join(', ')}]`);
    lines.push('```');
    return lines.join('\n');
}

function renderReadme(rows) {
    const fmt = (n) => Number(n).toLocaleString('en-US');
    const kloc = (n) => Math.round(Number(n) / 1000);
    const dates = rows.map((r) => r.date);

    const sourceChart = mermaidChart(
        'Source LOC by language (thousands of lines)', 'KLOC', dates,
        ['TypeScript', 'HTML', 'Markdown', 'CSS', 'JavaScript'].map((g) => rows.map((r) => kloc(r[g])))
    );
    const totalsChart = mermaidChart(
        'Source vs SQL vs JSON (thousands of lines)', 'KLOC', dates,
        [rows.map((r) => kloc(r.SourceTotal)), rows.map((r) => kloc(r.SQL)), rows.map((r) => kloc(r.JSON))]
    );

    const latest = rows[rows.length - 1];
    const lines = [
        '# Repository Stats',
        '',
        'Lines-of-code snapshots over time, counted with [cloc](https://github.com/AlDanial/cloc) over git-tracked files',
        '(`node_modules`, `dist`, and other ignored paths are excluded automatically).',
        '',
        'To record a new snapshot:',
        '',
        '```bash',
        'node stats/repo-stats.mjs              # current tree',
        'node stats/repo-stats.mjs <commit>     # backfill a historical commit',
        '```',
        '',
        `**Latest** (${latest.date}, \`${latest.commit.slice(0, 10)}\`): ` +
            `**${fmt(latest.SourceTotal)}** source LOC · **${fmt(latest.GrandTotal)}** total LOC · ${fmt(latest.Files)} files`,
        '',
        '## Source code over time',
        '',
        'Lines in top-to-bottom legend order: **TypeScript, HTML, Markdown, CSS, JavaScript**.',
        '',
        sourceChart,
        '',
        '## Source vs generated/data over time',
        '',
        'Lines: **Source total (TS+JS+HTML+CSS+MD), SQL, JSON**. SQL is dominated by migrations',
        '(including generated CodeGen runs); JSON is mostly declarative metadata.',
        '',
        totalsChart,
        '',
        '## History',
        '',
        '| Date | Commit | TypeScript | JavaScript | HTML | CSS | Markdown | Source Total | SQL | JSON | Other | Grand Total | Files |',
        '|---|---|---:|---:|---:|---:|---:|---:|---:|---:|---:|---:|---:|',
        ...rows.map((r) =>
            `| [${r.date}](reports/${r.date}.md) | \`${r.commit.slice(0, 10)}\` | ${fmt(r.TypeScript)} | ${fmt(r.JavaScript)} | ${fmt(r.HTML)} | ` +
            `${fmt(r.CSS)} | ${fmt(r.Markdown)} | **${fmt(r.SourceTotal)}** | ${fmt(r.SQL)} | ${fmt(r.JSON)} | ${fmt(r.Other)} | ${fmt(r.GrandTotal)} | ${fmt(r.Files)} |`
        ),
        '',
        'Full per-language breakdowns for each snapshot are in [reports/](reports/). Raw time series: [data.csv](data.csv).',
        '',
    ];
    writeFileSync(join(statsDir, 'README.md'), lines.join('\n'));
}

function snapshot(commitish) {
    const sha = commitish ? git('rev-parse', commitish) : git('rev-parse', 'HEAD');
    const date = commitish ? git('log', '-1', '--format=%cs', sha) : new Date().toISOString().slice(0, 10);
    console.log(`Counting ${commitish ? sha.slice(0, 10) : 'working tree'} (${date})...`);
    const counts = groupCounts(runCloc(commitish ? sha : null));
    writeReport(date, sha, counts);
    return {
        date,
        commit: sha,
        ...Object.fromEntries(Object.keys(LANGUAGE_GROUPS).map((g) => [g, counts.groups[g]])),
        Other: counts.groups.Other,
        SourceTotal: counts.sourceTotal,
        GrandTotal: counts.grandTotal,
        Files: counts.files,
    };
}

mkdirSync(reportsDir, { recursive: true });
const targets = process.argv.slice(2);
const rows = loadCsv();
if (targets.length === 0) {
    upsertRow(rows, snapshot(null));
} else {
    for (const t of targets) upsertRow(rows, snapshot(t));
}
saveCsv(rows);
rows.sort((a, b) => a.date.localeCompare(b.date));
renderReadme(rows);
console.log(`Done: ${rows.length} snapshot(s) in stats/data.csv; README regenerated.`);
