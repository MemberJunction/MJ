/**
 * Integration test for file storage Actions against a real MJ_v5 database
 * and Box.com storage account.
 *
 * Prerequisites:
 * - MJAPI running on http://localhost:4000 pointed at MJ_v5
 * - Box.com FileStorageAccount seeded in MJ_v5
 *
 * Run with:
 *   cd packages/Actions/CoreActions && npx vitest run src/__tests__/file-storage-integration.test.ts
 */

import { describe, it, expect, beforeAll } from 'vitest';
import { UserInfo } from '@memberjunction/core';
import { setupGraphQLClient } from '@memberjunction/graphql-dataprovider';
import { PDFGeneratorAction } from '../custom/files/pdf-generator.action';
import { PDFExtractorAction } from '../custom/files/pdf-extractor.action';
import type { RunActionParams } from '@memberjunction/actions-base';

// ── Config ────────────────────────────────────────────────────────────────────

const GRAPHQL_URL = process.env.MJAPI_URL
    ? `${process.env.MJAPI_URL}/graphql`
    : 'http://localhost:4000/graphql';
const HAS_SERVER = !!process.env.MJAPI_URL;

// ── Helpers ───────────────────────────────────────────────────────────────────

function makeParams(pairs: Record<string, unknown>, contextUser: UserInfo): RunActionParams {
    return {
        ContextUser: contextUser,
        Params: Object.entries(pairs).map(([Name, Value]) => ({
            Name,
            Value,
            Type: 'Input',
        })),
    } as RunActionParams;
}

// ── Tests ─────────────────────────────────────────────────────────────────────

describe.skipIf(!HAS_SERVER)('File Storage Integration', () => {
    let contextUser: UserInfo;

    beforeAll(async () => {
        // Initialize the GraphQL data provider against MJ_v5
        await setupGraphQLClient({ url: GRAPHQL_URL });

        // Get the current user from the provider
        const { Metadata } = await import('@memberjunction/core');
        const md = new Metadata();
        contextUser = md.CurrentUser;

        if (!contextUser) {
            throw new Error('No current user — is MJAPI running at ' + GRAPHQL_URL + '?');
        }
    }, 30_000);

    it('PDF Generator: generates a PDF and saves to Box.com storage', async () => {
        const action = new PDFGeneratorAction();

        const params = makeParams({
            Content: '<h1>Integration Test</h1><p>Generated by MJ file storage integration test.</p>',
            ContentType: 'html',
            FileName: 'integration-test.pdf',
            // Passing any truthy value triggers saveToMJStorage
            OutputFileID: 'save',
        }, contextUser);

        const result = await (action as unknown as Record<string, (...args: unknown[]) => Promise<unknown>>)['InternalRunAction'](params);

        expect(result).toMatchObject({ Success: true, ResultCode: 'SUCCESS_SAVED' });

        const generatedFileID = params.Params.find(p => p.Name === 'GeneratedFileID')?.Value as string;
        expect(generatedFileID).toBeTruthy();

        console.log('Generated FileID:', generatedFileID);

        // ── Round-trip: load the saved file back via PDF Extractor ──────────
        const extractor = new PDFExtractorAction();
        const extractParams = makeParams({ FileID: generatedFileID }, contextUser);

        const extractResult = await (extractor as unknown as Record<string, (...args: unknown[]) => Promise<unknown>>)['InternalRunAction'](extractParams);

        expect(extractResult).toMatchObject({ Success: true });

        const extractedText = extractParams.Params.find(p => p.Name === 'ExtractedText')?.Value as string;
        expect(extractedText).toContain('Integration Test');

        console.log('Extracted text preview:', extractedText?.slice(0, 200));
    }, 60_000);
});
