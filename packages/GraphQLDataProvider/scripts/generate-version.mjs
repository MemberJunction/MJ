#!/usr/bin/env node
/**
 * Reads this package's version from package.json and writes it as a TypeScript
 * constant in src/version.generated.ts so runtime code can reference it.
 *
 * Used by BrowserIndexedDBStorageProvider to derive the IndexedDB DB_VERSION
 * (major * 1000 + minor) — every minor release of @memberjunction/graphql-dataprovider
 * triggers a one-time IDB schema upgrade that wipes cached data, so cache format
 * changes shipped in a minor release naturally roll out without needing a manual
 * version bump in code.
 *
 * Runs as a prebuild step. Output file is committed (so it exists for editor type
 * checking and first-time clones), but always regenerated to stay in sync with
 * package.json.
 */
import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';

const here = dirname(fileURLToPath(import.meta.url));
const pkgPath = resolve(here, '..', 'package.json');
const outPath = resolve(here, '..', 'src', 'version.generated.ts');

const pkg = JSON.parse(readFileSync(pkgPath, 'utf-8'));
const version = pkg.version;

if (!version || typeof version !== 'string') {
    throw new Error(`generate-version: package.json has no valid 'version' field`);
}

const content = `// ⚠️ AUTO-GENERATED by scripts/generate-version.mjs — do not edit by hand.
// Regenerated on every build from package.json's version field.
//
// Used by BrowserIndexedDBStorageProvider to derive the IndexedDB DB_VERSION
// so cache schema upgrades fire automatically on minor version bumps.

export const PACKAGE_VERSION = '${version}';
`;

writeFileSync(outPath, content, 'utf-8');
console.log(`[generate-version] Wrote PACKAGE_VERSION='${version}' → ${outPath}`);
