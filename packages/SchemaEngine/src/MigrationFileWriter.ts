/**
 * MigrationFileWriter — wraps DDL in properly-named Flyway migration files.
 * Generic: no integration-specific assumptions.
 * File-emission only — returns EmittedFile objects, never writes to disk.
 */
import type { EmittedFile, MigrationMetadata } from './interfaces.js';

/**
 * Produces Flyway-compatible migration file names and content.
 */
export class MigrationFileWriter {
    /**
     * Generate a Flyway-compatible migration file name.
     * Format: {migrationsDir}/V{YYYYMMDDHHMM}__v{VERSION}.x_{Consumer}_{ObjectName}_{Action}.sql
     *
     * Example: migrations/v5/V202603120230__v5.10.x_Integration_HubSpot_CreateTables.sql
     */
    GenerateMigrationFileName(
        objectName: string,
        action: string,
        mjVersion: string,
        migrationsDir: string,
        timestamp: Date,
        consumer = 'Schema'
    ): string {
        const ts = FormatTimestamp(timestamp);
        const safeObj = SanitizeForFileName(objectName);
        const safeAction = SanitizeForFileName(action);
        const safeConsumer = SanitizeForFileName(consumer);
        return `${migrationsDir}/V${ts}__v${mjVersion}.x_${safeConsumer}_${safeObj}_${safeAction}.sql`;
    }

    /**
     * Wrap DDL content in a migration file with a standard header comment.
     */
    WrapInMigrationFile(ddl: string, metadata: MigrationMetadata, filePath: string): EmittedFile {
        const header = [
            `-- ============================================================`,
            `-- Auto-generated by MJ SchemaEngine`,
            `-- Consumer: ${metadata.SourceType} | Object: ${metadata.ObjectName}`,
            `-- Action: ${metadata.Action}`,
            `-- Generated: ${metadata.Timestamp}`,
            `-- Generated By: ${metadata.GeneratedBy}`,
            `-- ============================================================`,
            `-- NOTE: __mj_CreatedAt, __mj_UpdatedAt, FK indexes, views, and SPs`,
            `--       are created by CodeGen — do NOT add them here.`,
            `-- NOTE: Soft foreign keys are defined in additionalSchemaInfo,`,
            `--       applied by CodeGen — no FK constraints needed in this migration.`,
            `-- ============================================================`,
            ``,
        ].join('\n');

        return {
            FilePath: filePath,
            Content: header + ddl + '\n',
            Description: `Migration: ${metadata.Action} for ${metadata.SourceType}.${metadata.ObjectName}`,
        };
    }

    /**
     * Generate a sequence of timestamps, each 1 minute apart,
     * for ordering multiple migrations produced in a single pass.
     */
    GenerateTimestampSequence(count: number, startTime: Date = new Date()): Date[] {
        const result: Date[] = [];
        for (let i = 0; i < count; i++) {
            result.push(new Date(startTime.getTime() + i * 60_000));
        }
        return result;
    }
}

// ─── Helpers ────────────────────────────────────────────────────────

/** Format a Date as YYYYMMDDHHMM for Flyway versioning. */
export function FormatTimestamp(date: Date): string {
    const y = date.getUTCFullYear();
    const mo = String(date.getUTCMonth() + 1).padStart(2, '0');
    const d = String(date.getUTCDate()).padStart(2, '0');
    const h = String(date.getUTCHours()).padStart(2, '0');
    const mi = String(date.getUTCMinutes()).padStart(2, '0');
    return `${y}${mo}${d}${h}${mi}`;
}

/** Remove characters unsafe for Flyway file names. */
export function SanitizeForFileName(input: string): string {
    return input.replace(/[^A-Za-z0-9_]/g, '');
}
