/**
 * Connector for the Reach 360 LMS REST API.
 *
 * Reach 360 is Articulate's lightweight cloud LMS. The API is documented only
 * as HTML articles (no OpenAPI spec) and exposes ~25 endpoints across Courses,
 * Users, Groups, Learning Paths, Reports, Invitations and Completions.
 *
 * Auth: a single static Bearer token generated in the Reach 360 admin UI. No
 * OAuth flow, no refresh tokens. The token is sent on every request as
 * `Authorization: Bearer {apiKey}`.
 *
 * Pagination: cursor-based. List responses include a `nextUrl` pointer that
 * encodes the next cursor; follow until null.
 *
 * Incremental sync: Reach 360 has no CDC endpoint and no server-side date
 * filtering on list endpoints. Instead, `/reports/activity` acts as a
 * session-level change stream; callers filter client-side by `completedAt`.
 */
