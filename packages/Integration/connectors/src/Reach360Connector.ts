/**
 * Connection configuration for the Reach360 connector.
 * Reach 360 uses a single static Bearer token generated in the admin UI.
 */
export interface Reach360ConnectionConfig {
    /** Reach 360 API token (Bearer). Generated by an admin user in the Reach 360 UI. */
    apiKey: string;

    // ── Optional performance overrides ──────────────────────────────
    /** Maximum retries for rate-limited or failed requests. Default: 3 */
    MaxRetries?: number;
    /** HTTP request timeout in milliseconds. Default: 30000 */
    RequestTimeoutMs?: number;
    /** Minimum milliseconds between requests to avoid rate limiting. Default: 250 */
    MinRequestIntervalMs?: number;
}

/** Extended auth context with Reach360-specific config reference. */
interface Reach360AuthContext extends RESTAuthContext {
    Config: Reach360ConnectionConfig;
}

/** Shape of the cursor-paginated response envelope used by all Reach 360 list endpoints. */
interface Reach360PagedResponse {
    nextUrl?: string | null;
    [dataKey: string]: unknown;
}

