import { describe, it, expect } from 'vitest';
import { MigrationFileWriter } from '../MigrationFileWriter.js';

describe('MigrationFileWriter', () => {
    const writer = new MigrationFileWriter();

    describe('GenerateMigrationFileName', () => {
        it('should produce Flyway-compatible file names', () => {
            const ts = new Date(Date.UTC(2026, 2, 5, 14, 30)); // 2026-03-05 14:30 UTC
            const name = writer.GenerateMigrationFileName('HubSpot', 'CreateContactTable', '5.6.0', 'migrations/v2', ts);
            expect(name).toBe('migrations/v2/V202603051430__v5.6.0.x_Integration_HubSpot_CreateContactTable.sql');
        });

        it('should sanitize special characters from source type and action', () => {
            const ts = new Date(Date.UTC(2026, 0, 1, 0, 0));
            const name = writer.GenerateMigrationFileName('Your Membership!', 'Create-Deal Table', '5.6.0', 'migrations/v2', ts);
            expect(name).toContain('YourMembership');
            expect(name).toContain('CreateDealTable');
            expect(name).not.toContain(' ');
            expect(name).not.toContain('!');
            expect(name).not.toContain('-');
        });
    });

    describe('WrapInMigrationFile', () => {
        it('should include header with metadata', () => {
            const file = writer.WrapInMigrationFile('CREATE TABLE test;', {
                SourceType: 'HubSpot',
                ObjectName: 'Contact',
                Action: 'CreateContactTable',
                GeneratedBy: 'Test',
                Timestamp: '2026-03-05T00:00:00Z',
            }, 'migrations/v2/test.sql');

            expect(file.FilePath).toBe('migrations/v2/test.sql');
            expect(file.Content).toContain('Auto-generated by MJ Integration Schema Builder');
            expect(file.Content).toContain('Source: HubSpot | Object: Contact');
            expect(file.Content).toContain('Action: CreateContactTable');
            expect(file.Content).toContain('CodeGen');
            expect(file.Content).toContain('CREATE TABLE test;');
            expect(file.Description).toContain('CreateContactTable');
        });
    });

    describe('GenerateTimestampSequence', () => {
        it('should produce sequential timestamps 1 minute apart', () => {
            const start = new Date(Date.UTC(2026, 0, 1, 12, 0));
            const seq = writer.GenerateTimestampSequence(3, start);
            expect(seq).toHaveLength(3);
            expect(seq[0].getTime()).toBe(start.getTime());
            expect(seq[1].getTime()).toBe(start.getTime() + 60_000);
            expect(seq[2].getTime()).toBe(start.getTime() + 120_000);
        });

        it('should return empty array for count 0', () => {
            expect(writer.GenerateTimestampSequence(0)).toHaveLength(0);
        });
    });
});
