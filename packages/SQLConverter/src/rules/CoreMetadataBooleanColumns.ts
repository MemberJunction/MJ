/**
 * Boolean-column catalog for MemberJunction's pre-existing (baseline) tables.
 *
 * WHY THIS EXISTS
 * ---------------
 * `castBooleanInsertValues` / `convertBooleanLiteralComparisons` rewrite SQL Server
 * BIT literals (`0`/`1`) to PostgreSQL `FALSE`/`TRUE` for columns known to be BOOLEAN.
 * They learn column types from CREATE TABLE statements seen *within the same migration*
 * (via `ConversionContext.TableColumns`). That works for a migration's own new tables,
 * but CodeGen also emits metadata INSERTs/UPDATEs against the long-lived MJ tables
 * (Entity, EntityField, EntityPermission, User, …) that were created back in the
 * baseline, not in the migration being converted. Without their column types, those
 * `0`/`1` literals pass through unchanged and PG rejects them ("column is of type
 * boolean but expression is of type integer").
 *
 * So we seed EVERY baseline table's boolean columns into each fresh ConversionContext.
 * A migration that genuinely re-creates one of these tables overwrites the seeded entry
 * via CreateTableRule, so the seed is a safe default, never an override.
 *
 * HOW TO REGENERATE
 * -----------------
 * The source of truth is the latest baseline PG migration. Regenerate after a baseline
 * bump with (run from the repo root, point at the newest B*__Baseline.pg.sql):
 *
 *   awk '
 *     /^CREATE TABLE __mj\."/ { if (match($0, /"[A-Za-z0-9_]+"/)) { tbl=substr($0,RSTART+1,RLENGTH-2); inblk=1; delete cols; n=0 } next }
 *     inblk && /BOOLEAN/ { if (match($0, /"[A-Za-z0-9_]+"/)) cols[n++]=substr($0,RSTART+1,RLENGTH-2) }
 *     inblk && /^\);/ { if (n>0){ printf "  %s: [", tbl; for(i=0;i<n;i++) printf "%s\x27%s\x27", (i?", ":""), cols[i]; print "]," } inblk=0 }
 *   ' migrations-pg/v5/B*__Baseline.pg.sql
 */

