import { describe, it, expect } from 'vitest';
import { IncrementalBaker, stripVolatileHeaders } from '../IncrementalBaker.js';
import type { BakerWorkingDB, CapturedEntitySQL } from '../IncrementalBaker.js';
import type { TSQLToPGTranspiler } from '../MigrationConverter.js';

/** Transpiler stub — echoes the kept T-SQL unchanged (the baker's orchestration is under test,
 *  not the AST dialect). No Python needed. */
const echoTranspiler: TSQLToPGTranspiler = {
  async transpile(tsql: string) {
    return { sql: [tsql], unhandled: [] };
  },
};

/** Records the order of working-DB operations and returns canned capture output per entity. */
class FakeDB implements BakerWorkingDB {
  calls: string[] = [];
  applied: string[] = [];
  constructor(private readonly capture: (name: string) => CapturedEntitySQL) {}
  async apply(sql: string): Promise<void> {
    this.calls.push('apply');
    this.applied.push(sql);
  }
  async refreshMetadata(): Promise<void> {
    this.calls.push('refresh');
  }
  async captureEntity(name: string): Promise<CapturedEntitySQL> {
    this.calls.push(`capture:${name}`);
    return this.capture(name);
  }
}

/** Build a minimal SS migration: hand DDL on top, a CodeGen block naming `entities` below. */
const ssMigration = (handDDL: string, entities: string[]) => {
  const blocks = entities.map((e) => {
    const cls = e.replace(/[^A-Za-z0-9]/g, '');
    return [
      `/* spCreate SQL for ${e} */`,
      '-----------------------------------------------------------------',
      '-- SQL Code Generation',
      `-- Entity: ${e}`,
      `-- Item: spCreate${cls}`,
      '-- This was generated by the MemberJunction CodeGen tool.',
      '-----------------------------------------------------------------',
      `CREATE PROCEDURE [\${flyway:defaultSchema}].[spCreate${cls}] AS BEGIN SELECT 1; END;`,
      'GO',
    ].join('\n');
  });
  return [handDDL, '', ...blocks].join('\n');
};

const capturedFor = (name: string): CapturedEntitySQL => ({
  sql: [
    '-- ============================================================',
    `-- PostgreSQL Generated SQL for Entity: ${name}`,
    '-- Generated at: 2026-06-18T18:30:28.355Z',
    '-- ============================================================',
    `CREATE OR REPLACE VIEW __mj."vw${name.replace(/[^A-Za-z0-9]/g, '')}s" AS SELECT 1;`,
    '-- Generated at: 2026-06-18T18:30:28.368Z',
    `GRANT SELECT ON __mj."vw${name.replace(/[^A-Za-z0-9]/g, '')}s" TO "cdp_UI";`,
  ].join('\n'),
  permissionsSQL: `GRANT SELECT ON __mj."vw" TO "cdp_UI";`,
});

describe('IncrementalBaker', () => {
  it('drives the loop in order: apply hand body → refresh → capture each entity', async () => {
    const db = new FakeDB(capturedFor);
    const baker = new IncrementalBaker({ transpiler: echoTranspiler, db });
    const sql = ssMigration('ALTER TABLE [${flyway:defaultSchema}].[AIAgentRun] ADD LastHeartbeatAt DATETIMEOFFSET NULL;', ['MJ: AI Agent Runs']);

    await baker.bakeMigration(sql, 'V1__Heartbeat.sql');

    expect(db.calls).toEqual(['apply', 'refresh', 'capture:MJ: AI Agent Runs']);
  });

  it('captures every affected entity, in E(M) order (incl. cascade spDelete entities)', async () => {
    const db = new FakeDB(capturedFor);
    const baker = new IncrementalBaker({ transpiler: echoTranspiler, db });
    const sql = ssMigration('ALTER TABLE [${flyway:defaultSchema}].[AIPromptRun] ADD CacheHit BIT NULL;', [
      'MJ: AI Prompt Runs',
      'MJ: AI Agents',
      'MJ: AI Configurations',
    ]);

    const r = await baker.bakeMigration(sql, 'V2__Cache.sql');

    expect(r.affectedEntities).toEqual(['MJ: AI Prompt Runs', 'MJ: AI Agents', 'MJ: AI Configurations']);
    expect(db.calls).toEqual([
      'apply',
      'refresh',
      'capture:MJ: AI Prompt Runs',
      'capture:MJ: AI Agents',
      'capture:MJ: AI Configurations',
    ]);
  });

  it('assembles a standalone .pg.sql: baked header, hand body, then a CodeGen section per entity', async () => {
    const db = new FakeDB(capturedFor);
    const baker = new IncrementalBaker({ transpiler: echoTranspiler, db });
    const sql = ssMigration('ALTER TABLE [${flyway:defaultSchema}].[AIAgentRun] ADD LastHeartbeatAt DATETIMEOFFSET NULL;', ['MJ: AI Agent Runs']);

    const r = await baker.bakeMigration(sql, 'V1__Heartbeat.sql');

    expect(r.pgSQL).toContain('INLINE NATIVE CodeGen baking');
    expect(r.pgSQL).toContain('V1__Heartbeat.sql');
    expect(r.pgSQL).toContain('CREATE SCHEMA IF NOT EXISTS __mj;');
    // hand body (transpiled — echo keeps the ALTER) appears before the codegen section
    expect(r.pgSQL).toContain('LastHeartbeatAt');
    expect(r.pgSQL).toContain('-- ===================== CodeGen (native PG, baked) =====================');
    expect(r.pgSQL).toContain('CREATE OR REPLACE VIEW __mj."vwMJAIAgentRunss"'); // sanity: native view baked in
    const codegenIdx = r.pgSQL.indexOf('CodeGen (native PG, baked)');
    const handIdx = r.pgSQL.indexOf('LastHeartbeatAt');
    expect(handIdx).toBeGreaterThan(0);
    expect(codegenIdx).toBeGreaterThan(handIdx);
  });

  it('strips volatile `-- Generated at:` lines from the baked output (determinism)', async () => {
    const db = new FakeDB(capturedFor);
    const baker = new IncrementalBaker({ transpiler: echoTranspiler, db });
    const sql = ssMigration('ALTER TABLE [${flyway:defaultSchema}].[AIAgentRun] ADD LastHeartbeatAt DATETIMEOFFSET NULL;', ['MJ: AI Agent Runs']);

    const r = await baker.bakeMigration(sql, 'V1__Heartbeat.sql');

    expect(r.pgSQL).not.toMatch(/-- Generated at:/);
  });

  it('re-bakes byte-identically for unchanged input (deterministic capture)', async () => {
    const baker = new IncrementalBaker({ transpiler: echoTranspiler, db: new FakeDB(capturedFor) });
    const sql = ssMigration('ALTER TABLE [${flyway:defaultSchema}].[AIAgentRun] ADD LastHeartbeatAt DATETIMEOFFSET NULL;', ['MJ: AI Agent Runs']);
    const a = await baker.bakeMigration(sql, 'V1__Heartbeat.sql');
    const b = await new IncrementalBaker({ transpiler: echoTranspiler, db: new FakeDB(capturedFor) }).bakeMigration(sql, 'V1__Heartbeat.sql');
    expect(a.pgSQL).toBe(b.pgSQL);
  });

  it('applies the hand body (with a search_path) before any capture', async () => {
    const db = new FakeDB(capturedFor);
    const baker = new IncrementalBaker({ transpiler: echoTranspiler, db });
    const sql = ssMigration('ALTER TABLE [${flyway:defaultSchema}].[AIAgentRun] ADD LastHeartbeatAt DATETIMEOFFSET NULL;', ['MJ: AI Agent Runs']);

    await baker.bakeMigration(sql, 'V1__Heartbeat.sql');

    expect(db.applied).toHaveLength(1);
    expect(db.applied[0]).toContain('SET search_path TO __mj, public;');
    expect(db.applied[0]).toContain('LastHeartbeatAt');
  });

  it('propagates conversion status + hand-procedural gaps from convertMigration', async () => {
    const db = new FakeDB(capturedFor);
    const baker = new IncrementalBaker({ transpiler: echoTranspiler, db });
    // A hand-written routine in the hand region → needs-hand-authoring.
    const sql = [
      'CREATE TABLE [${flyway:defaultSchema}].[Job] (ID UNIQUEIDENTIFIER NOT NULL);',
      'GO',
      'CREATE OR ALTER PROCEDURE [${flyway:defaultSchema}].[spClaimNextJob] AS BEGIN SELECT 1; END;',
    ].join('\n');

    const r = await baker.bakeMigration(sql, 'V3__Sprocs.sql');

    expect(r.status).toBe('needs-hand-authoring');
    expect(r.handProcedural.length).toBeGreaterThan(0);
  });

  describe('re-bake mode (committedPgSql provided)', () => {
    it('advances via the committed file (not the hand body) and bakes native, mode=baked', async () => {
      const db = new FakeDB(capturedFor);
      const baker = new IncrementalBaker({ transpiler: echoTranspiler, db });
      const sql = ssMigration('ALTER TABLE [${flyway:defaultSchema}].[AIAgentRun] ADD LastHeartbeatAt DATETIMEOFFSET NULL;', ['MJ: AI Agent Runs']);
      const committed = '-- committed pg.sql for V1\nCREATE OR REPLACE VIEW __mj."vwAIAgentRuns" AS SELECT 1;';

      const r = await baker.bakeMigration(sql, 'V1__Heartbeat.sql', committed);

      expect(r.mode).toBe('baked');
      // working DB advanced via the COMMITTED file, not the transpiled hand body
      expect(db.applied).toContain(committed);
      expect(db.applied.some((s) => s.includes('LastHeartbeatAt') && !s.includes('committed pg.sql'))).toBe(false);
      // captured native codegen still assembled into the output
      expect(r.pgSQL).toContain('CodeGen (native PG, baked)');
      expect(db.calls.filter((c) => c.startsWith('capture:'))).toEqual(['capture:MJ: AI Agent Runs']);
    });

    it('preserves the committed file (mode=preserved) when the migration has transpile gaps', async () => {
      // Transpiler that reports an unhandled statement → re-bake must fall back to committed.
      const gapTranspiler = { async transpile(tsql: string) { return { sql: [tsql], unhandled: [{ kind: 'X', snippet: 's' }] }; } };
      const db = new FakeDB(capturedFor);
      const baker = new IncrementalBaker({ transpiler: gapTranspiler, db });
      const sql = ssMigration('ALTER TABLE [${flyway:defaultSchema}].[AIAgentRun] ADD LastHeartbeatAt DATETIMEOFFSET NULL;', ['MJ: AI Agent Runs']);
      const committed = '-- hand-verified committed pg.sql';

      const r = await baker.bakeMigration(sql, 'V1__Heartbeat.sql', committed);

      expect(r.mode).toBe('preserved');
      expect(r.pgSQL).toBe(committed);
      // never captured — we kept the committed file wholesale
      expect(db.calls.some((c) => c.startsWith('capture:'))).toBe(false);
    });
  });
});

describe('stripVolatileHeaders', () => {
  it('removes only the `-- Generated at:` lines, leaving SQL intact', () => {
    const input = ['-- Generated at: 2026-06-18T18:30:28.355Z', 'CREATE VIEW x AS SELECT 1;', '  -- Generated at: 2026-06-18T18:30:28.368Z'].join('\n');
    expect(stripVolatileHeaders(input)).toBe('CREATE VIEW x AS SELECT 1;');
  });
});
