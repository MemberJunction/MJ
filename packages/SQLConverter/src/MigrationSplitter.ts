/**
 * MigrationSplitter — deterministic decomposition of a SQL Server migration into
 * the regions that drive the "split-and-regenerate" PG conversion strategy.
 *
 * See `plans/pg-migration-architecture/SPLIT_AND_REGENERATE_PROPOSAL.md`.
 *
 * The premise: almost all migration content is emitted by one of two MJ
 * generators — CodeGen (SQL objects: views/sprocs/GRANTs/indexes/extended
 * properties) and mj-sync (metadata seed via `DECLARE @x_<hash>; EXEC spCreate…`).
 * Each generator has a FIXED output grammar, so its output is identified
 * deterministically rather than parsed as arbitrary T-SQL. What remains is a
 * small, regular hand-authored remainder plus a rare hand-written procedural
 * residue that must be human-authored for PG.
 *
 * This module performs NO conversion. It only classifies, so the pipeline can
 * route each region: regenerate (CodeGen objects), re-seed/transpile (mj-sync),
 * transpile (regular DDL), or escalate to a human (hand procedural).
 */

/** What a contiguous region of a migration was produced by / consists of. */
export type MigrationRegionKind =
  /** Regular hand-authored DDL/DML — CREATE/ALTER TABLE, indexes, constraints, plain INSERT/UPDATE. Transpilable. */
  | 'hand-ddl'
  /** Banner-marked CodeGen SQL objects — discard on PG and regenerate natively. */
  | 'codegen-objects'
  /** mj-sync metadata seed — session header or `@x_<hash>` vars. Re-seed via `mj sync push`. */
  | 'metadata-sync'
  /** Real schema DDL — `CREATE/ALTER TABLE`, indexes, constraints. Transpiled. */
  | 'schema-ddl'
  /** Data DML — `INSERT/UPDATE/DELETE`. Transpiled when it isn't part of an mj-sync seed. */
  | 'data-dml'
  /** Hand-written procedural SQL — `CREATE [OR ALTER] PROCEDURE|FUNCTION|TRIGGER` authored by a dev. Needs a human PG version. */
  | 'hand-procedural';

/** How the CodeGen-block boundary was located. */
export type BoundaryMethod =
  /** Split at the explicit `-- CODE GEN RUN` delimiter line. */
  | 'code-gen-run-delimiter'
  /** Split at the `/*` opener of the first banner-marked CodeGen item. */
  | 'first-item-banner'
  /** No CodeGen block present in the file. */
  | 'no-codegen-block';

/** File-level routing recommendation derived from the region findings. */
export type FileRouting =
  /** Hand region is purely regular DDL/DML (plus an optional CodeGen block to drop). Fully automatable. */
  | 'transpile-only'
  /** Hand region contains mj-sync metadata blocks needing re-seed or narrow transpile. */
  | 'transpile-plus-reseed'
  /** Hand region contains hand-written procedural SQL — requires a human-authored `.pg.sql`. */
  | 'needs-hand-authoring';

/** A classified region detected in the hand-authored portion. */
export interface RegionFinding {
  kind: MigrationRegionKind;
  /** The matched signature (keyword / pattern) that produced this finding. */
  evidence: string;
  /** 1-based line number of the first occurrence. */
  line: number;
}

export interface MigrationSplitResult {
  fileName: string;
  totalLines: number;
  /** 1-based line where the CodeGen block starts, or null if there is none. */
  codeGenBoundaryLine: number | null;
  boundaryMethod: BoundaryMethod;
  /** Hand-authored text (everything above the boundary, or the whole file if no boundary). */
  handAuthored: string;
  /** CodeGen block text (everything from the boundary down); '' when there is no block. */
  codeGenBlock: string;
  /** Classification of the hand-authored region (codegen block is excluded — it is always `codegen-objects`). */
  handAuthoredRegions: RegionFinding[];
  routing: FileRouting;
}

/** Marker emitted by CodeGen inside every generated item's header comment block. */
const GENERATED_BANNER = /This was generated by the MemberJunction CodeGen tool/i;
/** Explicit run-level delimiter (present in a minority of files but unambiguous when it is). */
const CODE_GEN_RUN = /^--\s*CODE GEN RUN\b/i;
/** Opener of a CodeGen item header comment, e.g. `/* Base View SQL for ... *​/`. */
const ITEM_OPENER = /^\s*\/\*/;

/** Hand-written stored routine — distinct from CodeGen sprocs (which live below the boundary). */
const HAND_PROCEDURAL = /\bCREATE\s+(?:OR\s+ALTER\s+)?(?:PROCEDURE|PROC|FUNCTION|TRIGGER)\b/i;
/**
 * mj-sync session header (definitive). Emitted once at the top of a logging
 * session; the only reliable way to tell a `*_Metadata_Sync` push apart from a
 * hand-written `Backfill_*` DML migration. Matched on RAW lines (it lives in a
 * `-- Description:` comment that the code scan strips out).
 */
const MJSYNC_SESSION_HEADER = /MetadataSync push operation/i;
/** mj-sync's hashed local-variable signature, e.g. `@Name_da319a9d`. Only mj-sync emits these. */
const MJSYNC_HASHED_VAR = /@\w+_[0-9a-f]{8}\b/;
/** Real schema DDL — the genuine transpilation surface. */
const SCHEMA_DDL = /\b(CREATE\s+TABLE|ALTER\s+TABLE|CREATE\s+(?:UNIQUE\s+)?(?:CLUSTERED\s+|NONCLUSTERED\s+)?INDEX|DROP\s+(?:TABLE|INDEX|CONSTRAINT)|ADD\s+CONSTRAINT)\b/i;
/** Data DML — transpilable, but in an mj-sync file it is just the metadata seed.
 * No trailing \b: `UPDATE ${flyway:defaultSchema}.T` starts with `$` (non-word),
 * so a trailing word-boundary assertion would silently fail to match. The leading
 * `\b` + required whitespace (`\s+`) already prevents false matches inside words. */
const DATA_DML = /\b(INSERT\s+INTO|UPDATE\s+|DELETE\s+FROM)/i;

/** Max lines to walk up from a banner to its `/*` opener (header blocks are ~6-8 lines). */
const MAX_HEADER_WALKUP = 25;

/**
 * Split a migration into its hand-authored and CodeGen-generated regions and
 * recommend a routing. Pure function — no I/O.
 */
export function splitMigration(sql: string, fileName = '<inline>'): MigrationSplitResult {
  const lines = sql.split('\n');
  const boundary = findCodeGenBoundary(lines);
  const handLines = boundary.line == null ? lines : lines.slice(0, boundary.line - 1);
  const blockLines = boundary.line == null ? [] : lines.slice(boundary.line - 1);

  const handAuthored = handLines.join('\n');
  const handAuthoredRegions = classifyHandRegion(handLines);

  return {
    fileName,
    totalLines: lines.length,
    codeGenBoundaryLine: boundary.line,
    boundaryMethod: boundary.method,
    handAuthored,
    codeGenBlock: blockLines.join('\n'),
    handAuthoredRegions,
    routing: deriveRouting(handAuthoredRegions),
  };
}

/** Locate the first line of the CodeGen block, preferring the explicit delimiter. */
function findCodeGenBoundary(lines: string[]): { line: number | null; method: BoundaryMethod } {
  const runDelimiter = lines.findIndex((l) => CODE_GEN_RUN.test(l));
  if (runDelimiter !== -1) {
    return { line: runDelimiter + 1, method: 'code-gen-run-delimiter' };
  }

  const firstBanner = lines.findIndex((l) => GENERATED_BANNER.test(l));
  if (firstBanner === -1) {
    return { line: null, method: 'no-codegen-block' };
  }

  // Walk up from the banner to the `/*` opener of the item's header block.
  const opener = walkUpToItemOpener(lines, firstBanner);
  return { line: opener + 1, method: 'first-item-banner' };
}

/** From a banner line, find the nearest preceding `/*` opener (the item title line). */
function walkUpToItemOpener(lines: string[], bannerIdx: number): number {
  const floor = Math.max(0, bannerIdx - MAX_HEADER_WALKUP);
  for (let i = bannerIdx; i >= floor; i--) {
    if (ITEM_OPENER.test(lines[i])) {
      return i;
    }
  }
  // Fallback: no `/*` opener in the window — this item uses a `--` comment header
  // (e.g. `-- Item: vwAIAgentRuns` under a dash rule). Split at the start of the
  // banner's CONTIGUOUS comment/blank run only — backing up a fixed offset can land
  // mid-statement and bisect a preceding INSERT (live hit: AIAgentRun_LastHeartbeatAt,
  // whose registration INSERT lost its tail to the block and failed to parse).
  let i = bannerIdx;
  while (i - 1 >= floor && /^\s*(--.*)?$/.test(lines[i - 1])) {
    i--;
  }
  return i;
}

/**
 * Classify the hand-authored region. Scans on a comment/string-stripped copy so
 * keywords inside comments or string literals don't produce false findings, while
 * preserving original line numbers for evidence.
 */
function classifyHandRegion(handLines: string[]): RegionFinding[] {
  const findings: RegionFinding[] = [];
  const seen = new Set<MigrationRegionKind>();

  // The mj-sync session header lives in a comment, so detect it on raw lines
  // before stripping. This is the definitive metadata-sync signal.
  const headerIdx = handLines.findIndex((l) => MJSYNC_SESSION_HEADER.test(l));
  if (headerIdx !== -1) {
    addFindingOnce(findings, seen, { kind: 'metadata-sync', evidence: 'MetadataSync push operation' }, '', headerIdx + 1);
  }

  const code = stripCommentsPreservingLines(handLines);
  for (let i = 0; i < code.length; i++) {
    const line = code[i];
    if (line.trim().length === 0) continue;

    addFindingOnce(findings, seen, classifyCodeLine(line), line, i + 1);
  }
  return findings;
}

/** Classify a single (comment-stripped) code line into a region kind, if it matches a signature. */
function classifyCodeLine(line: string): { kind: MigrationRegionKind; evidence: string } | null {
  // Order matters: most-specific first. Procedural and mj-sync take precedence
  // over generic DDL/DML because they imply special handling.
  if (HAND_PROCEDURAL.test(line)) {
    return { kind: 'hand-procedural', evidence: (line.match(HAND_PROCEDURAL) ?? [''])[0].trim() };
  }
  if (MJSYNC_HASHED_VAR.test(line)) {
    return { kind: 'metadata-sync', evidence: (line.match(MJSYNC_HASHED_VAR) ?? [''])[0].trim() };
  }
  if (SCHEMA_DDL.test(line)) {
    return { kind: 'schema-ddl', evidence: (line.match(SCHEMA_DDL) ?? [''])[0].trim() };
  }
  if (DATA_DML.test(line)) {
    return { kind: 'data-dml', evidence: (line.match(DATA_DML) ?? [''])[0].trim() };
  }
  return null;
}

/** Record a finding only on first occurrence of its kind. */
function addFindingOnce(
  findings: RegionFinding[],
  seen: Set<MigrationRegionKind>,
  classified: { kind: MigrationRegionKind; evidence: string } | null,
  _line: string,
  lineNo: number,
): void {
  if (!classified || seen.has(classified.kind)) return;
  seen.add(classified.kind);
  findings.push({ kind: classified.kind, evidence: classified.evidence, line: lineNo });
}

/**
 * Return a copy of the lines with comment and string-literal content blanked out,
 * keeping the array length (and therefore line numbers) intact. Handles `--` line
 * comments, `/* *​/` block comments, and `'...'` string literals (doubled-quote escapes).
 */
function stripCommentsPreservingLines(lines: string[]): string[] {
  const out: string[] = [];
  let inBlockComment = false;
  // T-SQL string literals span lines (mj-sync seeds embed whole prompt templates),
  // so the in-string state must persist across lines exactly like block comments —
  // otherwise literal content containing "ALTER TABLE"/"CREATE TABLE" text is
  // scanned as code and misclassifies the file.
  let inString = false;

  for (const raw of lines) {
    let result = '';
    let i = 0;
    while (i < raw.length) {
      const two = raw.slice(i, i + 2);
      if (inBlockComment) {
        if (two === '*/') { inBlockComment = false; i += 2; } else { i += 1; }
        continue;
      }
      if (inString) {
        if (raw[i] === "'") {
          if (raw[i + 1] === "'") { i += 2; continue; } // escaped quote
          inString = false;
        }
        i += 1;
        continue;
      }
      if (two === '--') { break; }            // rest of line is a comment
      if (two === '/*') { inBlockComment = true; i += 2; continue; }
      if (raw[i] === "'") { inString = true; i += 1; continue; }
      result += raw[i];
      i += 1;
    }
    out.push(result);
  }
  return out;
}

/**
 * Derive the file-level routing from the hand-region findings.
 *
 * Order is significant:
 *   1. Any hand-written procedural SQL → human authoring (we never auto-translate it).
 *   2. Real schema DDL present → transpile (this is the genuine Category-B surface).
 *      A file with both schema DDL *and* mj-sync metadata is the rare mixed case —
 *      still routed to transpile so the DDL is not lost.
 *   3. mj-sync metadata with no schema DDL → re-seed (the INSERT/UPDATE/DELETE in
 *      such a file is the metadata seed, reproduced by `mj sync push`, not real DDL).
 *   4. Anything else with data DML to translate → transpile (e.g. a Backfill_*).
 *   5. Nothing translatable → transpile-only with empty output (pure CodeGen file).
 */
function deriveRouting(findings: RegionFinding[]): FileRouting {
  const has = (k: MigrationRegionKind) => findings.some((f) => f.kind === k);
  if (has('hand-procedural')) return 'needs-hand-authoring';
  if (has('schema-ddl')) return 'transpile-only';
  if (has('metadata-sync')) return 'transpile-plus-reseed';
  return 'transpile-only';
}
