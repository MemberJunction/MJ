import { describe, it, expect } from 'vitest';
import { FileContentExtractor } from '../file-content-extractor';

describe('FileContentExtractor', () => {
    describe('IsSupported', () => {
        it('returns true for PDF', () => {
            expect(FileContentExtractor.IsSupported('application/pdf')).toBe(true);
        });

        it('returns true for Excel', () => {
            expect(FileContentExtractor.IsSupported(
                'application/vnd.openxmlformats-officedocument.spreadsheetml.sheet'
            )).toBe(true);
        });

        it('returns true for Word', () => {
            expect(FileContentExtractor.IsSupported(
                'application/vnd.openxmlformats-officedocument.wordprocessingml.document'
            )).toBe(true);
        });

        it('returns false for images', () => {
            expect(FileContentExtractor.IsSupported('image/png')).toBe(false);
        });

        it('returns false for unknown types', () => {
            expect(FileContentExtractor.IsSupported('application/octet-stream')).toBe(false);
        });
    });

    describe('Extract', () => {
        it('returns empty string for unsupported MIME type', async () => {
            const result = await FileContentExtractor.Extract('dGVzdA==', 'image/png', 'test.png');
            expect(result).toBe('');
        });

        it('returns empty string for empty content', async () => {
            const result = await FileContentExtractor.Extract('', 'application/pdf', 'test.pdf');
            expect(result).toBe('');
        });

        it('extracts text from a PDF generated by pdfkit', async () => {
            // Generate a real PDF using pdfkit
            const PDFDocument = (await import('pdfkit')).default;
            const pdfBase64 = await new Promise<string>((resolve) => {
                const chunks: Buffer[] = [];
                const doc = new PDFDocument();
                doc.on('data', (c: Buffer) => chunks.push(c));
                doc.on('end', () => resolve(Buffer.concat(chunks).toString('base64')));
                doc.text('Hello World from PDF extraction test');
                doc.end();
            });

            const result = await FileContentExtractor.Extract(pdfBase64, 'application/pdf', 'test.pdf');
            expect(result).toContain('[PDF: test.pdf');
            expect(result).toContain('Hello World from PDF extraction test');
        }, 15_000);

        it('strips data URL prefix before extracting PDF', async () => {
            const PDFDocument = (await import('pdfkit')).default;
            const pdfBase64 = await new Promise<string>((resolve) => {
                const chunks: Buffer[] = [];
                const doc = new PDFDocument();
                doc.on('data', (c: Buffer) => chunks.push(c));
                doc.on('end', () => resolve(Buffer.concat(chunks).toString('base64')));
                doc.text('Data URL test');
                doc.end();
            });

            const dataUrl = `data:application/pdf;base64,${pdfBase64}`;
            const result = await FileContentExtractor.Extract(dataUrl, 'application/pdf', 'test.pdf');
            expect(result).toContain('Data URL test');
        }, 15_000);

        it('extracts text from an Excel file', async () => {
            const XLSX = await import('xlsx');
            const wb = XLSX.utils.book_new();
            const ws = XLSX.utils.aoa_to_sheet([
                ['Name', 'Value'],
                ['Alpha', 100],
                ['Beta', 200],
            ]);
            XLSX.utils.book_append_sheet(wb, ws, 'TestSheet');
            const buffer = XLSX.write(wb, { type: 'buffer', bookType: 'xlsx' });
            const base64 = Buffer.from(buffer).toString('base64');

            const result = await FileContentExtractor.Extract(
                base64,
                'application/vnd.openxmlformats-officedocument.spreadsheetml.sheet',
                'test.xlsx'
            );
            expect(result).toContain('[Excel: test.xlsx');
            expect(result).toContain('TestSheet');
            expect(result).toContain('Alpha');
            expect(result).toContain('Beta');
        });

        it('extracts text from a Word document', async () => {
            // Generate a minimal DOCX using the docx library
            const { Document, Packer, Paragraph, TextRun } = await import('docx');
            const doc = new Document({
                sections: [{
                    children: [
                        new Paragraph({
                            children: [new TextRun('Hello from Word extraction test')],
                        }),
                    ],
                }],
            });
            const buffer = await Packer.toBuffer(doc);
            const base64 = buffer.toString('base64');

            const result = await FileContentExtractor.Extract(
                base64,
                'application/vnd.openxmlformats-officedocument.wordprocessingml.document',
                'test.docx'
            );
            expect(result).toContain('[Word: test.docx');
            expect(result).toContain('Hello from Word extraction test');
        });

        it('returns empty string for invalid PDF content', async () => {
            const badBase64 = Buffer.from('not a pdf').toString('base64');
            const result = await FileContentExtractor.Extract(badBase64, 'application/pdf', 'bad.pdf');
            expect(result).toBe('');
        });

        it('uses default filename when not provided', async () => {
            const XLSX = await import('xlsx');
            const wb = XLSX.utils.book_new();
            XLSX.utils.book_append_sheet(wb, XLSX.utils.aoa_to_sheet([['A']]), 'S1');
            const buffer = XLSX.write(wb, { type: 'buffer', bookType: 'xlsx' });
            const base64 = Buffer.from(buffer).toString('base64');

            const result = await FileContentExtractor.Extract(
                base64,
                'application/vnd.openxmlformats-officedocument.spreadsheetml.sheet'
            );
            expect(result).toContain('[Excel: spreadsheet.xlsx');
        });
    });
});
