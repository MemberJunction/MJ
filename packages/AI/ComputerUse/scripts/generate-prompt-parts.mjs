#!/usr/bin/env node

/**
 * Generate shared prompt parts for the Computer Use package.
 *
 * Single-source-of-truth: the behavioral text shared between the metadata
 * templates (Layer 2, consumed by AIPromptRunner via {@include}) and the
 * standalone default prompts (Layer 1, MJ-independent) lives ONCE in the
 * leaf partials under:
 *   metadata/prompts/templates/computer-use/_includes/
 *
 * This script reads those partials and emits them as escaped TypeScript
 * string consts into src/prompts/prompt-parts.generated.ts, which the
 * default-controller.ts / default-judge.ts files compose into the full
 * DEFAULT_*_PROMPT constants. The metadata templates pull the same partials
 * via {@include}, so the two layers cannot silently diverge.
 *
 * Runs automatically as a prebuild step (see package.json).
 */

import { readFileSync, writeFileSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);

// Script is at: packages/AI/ComputerUse/scripts/
// The _includes partials live in metadata/prompts/templates/computer-use/_includes/
// at the repo root, which is 4 levels up from this script's directory.
const INCLUDES_DIR = join(__dirname, '../../../../metadata/prompts/templates/computer-use/_includes');
const OUTPUT_FILE = join(__dirname, '../src/prompts/prompt-parts.generated.ts');

/**
 * The partials to emit, mapped to their exported const names. Each value is
 * the verbatim file content, escaped via JSON.stringify (which handles
 * backticks, newlines, quotes, and unicode safely — no manual escaping).
 */
const PARTS = [
    { file: 'controller-actions.md', constName: 'CONTROLLER_ACTIONS' },
    { file: 'controller-response-format.md', constName: 'CONTROLLER_RESPONSE_FORMAT' },
    { file: 'judge-core.md', constName: 'JUDGE_CORE' }
];

/**
 * Read a partial and return its content with any trailing newline trimmed,
 * so composition in the consumers controls inter-block spacing precisely.
 */
function readPartial(fileName) {
    const fullPath = join(INCLUDES_DIR, fileName);
    return readFileSync(fullPath, 'utf8').replace(/\n+$/, '');
}

/**
 * Build the full TypeScript file content from the parts.
 */
function buildFileContent(parts) {
    const header = `// AUTO-GENERATED by scripts/generate-prompt-parts.mjs — do not edit
//
// These consts hold the shared, token-free behavioral text used by BOTH the
// Computer Use metadata templates (via {@include}) and the standalone default
// prompts in this package. Edit the source partials under
// metadata/prompts/templates/computer-use/_includes/ and re-run the prebuild
// (npm run prebuild) to regenerate this file.
`;

    const body = parts
        .map(({ file, constName }) => {
            const content = readPartial(file);
            return `export const ${constName} = ${JSON.stringify(content)};`;
        })
        .join('\n\n');

    return `${header}\n${body}\n`;
}

function main() {
    console.log('Generating Computer Use shared prompt parts...');

    try {
        const fileContent = buildFileContent(PARTS);
        writeFileSync(OUTPUT_FILE, fileContent, 'utf8');
        for (const { constName } of PARTS) {
            console.log(`  ✓ ${constName}`);
        }
        console.log(`✓ Wrote ${OUTPUT_FILE}`);
    } catch (error) {
        console.error('✗ Error generating prompt parts:', error.message);
        process.exit(1);
    }
}

main();
