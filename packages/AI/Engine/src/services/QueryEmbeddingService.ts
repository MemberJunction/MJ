import { SimpleVectorService, VectorEntry } from '@memberjunction/ai-vectors-memory';
import { MJAIModelEntityExtended } from '@memberjunction/ai-core-plus';
import { QueryMatchResult, QueryEmbeddingMetadata } from '../types/QueryMatchResult';
import { EmbedTextResult } from '@memberjunction/ai';
import { LogError, RunView, UserInfo } from '@memberjunction/core';

/**
 * Utility service for query catalog semantic search.
 *
 * Unlike AgentEmbeddingService (which generates embeddings at AIEngine startup),
 * this service loads pre-computed embeddings from the database. Embeddings are
 * generated by MJQueryEntityServer.Save() using composite text (Name + UserQuestion + Description)
 * and stored in the Query entity's EmbeddingVector column.
 *
 * This follows the same pattern as RefreshNoteEmbeddings in AIEngine — parsing
 * stored JSON vectors from DB records rather than calling the embedding API.
 *
 * All methods are static — no instance state required.
 *
 * @example
 * ```typescript
 * const vectorService = await QueryEmbeddingService.LoadQueryEmbeddings(contextUser);
 * const matches = await QueryEmbeddingService.FindSimilarQueries(
 *   vectorService,
 *   "monthly revenue by region",
 *   (text) => AIEngine.Instance.EmbedTextLocal(text),
 *   10,
 *   0.3
 * );
 * ```
 */
export class QueryEmbeddingService {
    /**
     * Load pre-computed query embeddings from the database into a SimpleVectorService.
     * Only loads queries that have a non-null EmbeddingVector.
     *
     * Follows the same pattern as AIEngine.RefreshNoteEmbeddings() — parsing
     * stored JSON vectors rather than generating embeddings at load time.
     *
     * @param contextUser - User context for RunView permissions
     * @param filter - Optional additional SQL filter (e.g., "Reusable = 1 AND Status = 'Approved'")
     * @returns SimpleVectorService loaded with query embeddings, ready for FindSimilarQueries()
     */
    public static async LoadQueryEmbeddings(
        contextUser: UserInfo,
        filter?: string
    ): Promise<SimpleVectorService<QueryEmbeddingMetadata>> {
        const rv = new RunView();
        const baseFilter = "EmbeddingVector IS NOT NULL";
        const extraFilter = filter ? `${baseFilter} AND ${filter}` : baseFilter;

        const result = await rv.RunView<{
            ID: string;
            Name: string;
            Description: string;
            Category: string;
            Status: string;
            Reusable: boolean;
            SQL: string;
            UserQuestion: string;
            EmbeddingVector: string;
        }>({
            EntityName: 'MJ: Queries',
            ExtraFilter: extraFilter,
            Fields: ['ID', 'Name', 'Description', 'Category', 'Status', 'Reusable', 'SQL', 'UserQuestion', 'EmbeddingVector'],
            ResultType: 'simple'
        }, contextUser);

        if (!result.Success) {
            LogError(`QueryEmbeddingService: Failed to load query embeddings: ${result.ErrorMessage}`);
            return new SimpleVectorService<QueryEmbeddingMetadata>();
        }

        const entries: VectorEntry<QueryEmbeddingMetadata>[] = [];

        for (const query of result.Results) {
            try {
                const vector = JSON.parse(query.EmbeddingVector);
                if (!Array.isArray(vector) || vector.length === 0) continue;

                entries.push({
                    key: query.ID,
                    vector,
                    metadata: {
                        id: query.ID,
                        name: query.Name,
                        description: query.Description || '',
                        category: query.Category || '',
                        status: query.Status || '',
                        reusable: query.Reusable ?? false,
                        sql: query.SQL || '',
                        userQuestion: query.UserQuestion || ''
                    }
                });
            } catch (error) {
                LogError(`QueryEmbeddingService: Failed to parse embedding for query ${query.Name}: ${error instanceof Error ? error.message : String(error)}`);
            }
        }

        const vectorService = new SimpleVectorService<QueryEmbeddingMetadata>();
        vectorService.LoadVectors(entries);
        return vectorService;
    }

    /**
     * Find queries semantically similar to a natural language search text.
     *
     * Follows the same pattern as AgentEmbeddingService.FindSimilarAgents() —
     * embeds the search text, then uses FindNearest for cosine similarity.
     *
     * @param vectorService - Vector service from LoadQueryEmbeddings()
     * @param searchText - Natural language description of what data is needed
     * @param embedFunction - Function to generate embeddings (typically from AIEngine.EmbedTextLocal)
     * @param topK - Maximum number of results to return (default: 10)
     * @param minSimilarity - Minimum similarity score 0-1 (default: 0.3)
     * @returns Array of matching queries sorted by similarity score (highest first)
     */
    public static async FindSimilarQueries(
        vectorService: SimpleVectorService<QueryEmbeddingMetadata>,
        searchText: string,
        embedFunction: (text: string) => Promise<{result: EmbedTextResult, model: MJAIModelEntityExtended} | null>,
        topK: number = 10,
        minSimilarity: number = 0.3
    ): Promise<QueryMatchResult[]> {
        if (!searchText || searchText.trim().length === 0) {
            throw new Error('searchText cannot be empty');
        }

        try {
            const queryEmbedding = await embedFunction(searchText);

            if (!queryEmbedding || !queryEmbedding.result || queryEmbedding.result.vector.length === 0) {
                throw new Error('Failed to generate embedding for search text');
            }

            const results = vectorService.FindNearest(
                queryEmbedding.result.vector,
                topK,
                minSimilarity,
                'cosine'
            );

            return results.map(r => ({
                queryId: r.key,
                queryName: r.metadata?.name || 'Unknown',
                description: r.metadata?.description || '',
                category: r.metadata?.category || '',
                similarityScore: r.score,
                status: r.metadata?.status || '',
                reusable: r.metadata?.reusable ?? false,
                sql: r.metadata?.sql,
                userQuestion: r.metadata?.userQuestion
            }));
        } catch (error) {
            LogError(`QueryEmbeddingService: Error finding similar queries: ${error instanceof Error ? error.message : String(error)}`);
            throw error;
        }
    }
}
