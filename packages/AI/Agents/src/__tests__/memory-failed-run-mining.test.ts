/**
 * Unit tests for the failed-run memory mining in MemoryManagerAgent
 * (LoadInstructiveFailedAgentRuns filter, markCorrectiveNotes, Ephemeral tier resolution).
 *
 * Mirror-style (see prompt-formatting.test.ts): the deterministic decision logic is
 * replicated here so it can be verified without an LLM or database. The LLM extraction
 * itself reuses the existing, separately-tested executeNoteExtraction pipeline.
 */
import { describe, it, expect } from 'vitest';

type NoteType = 'Preference' | 'Constraint' | 'Context' | 'Example' | 'Issue';
type Tier = 'Immutable' | 'Protected' | 'Standard' | 'Ephemeral';

interface ExtractedNote {
    type: NoteType;
    content: string;
    confidence: number;
    protectionTierHint?: Tier;
}

interface InstructiveFailedRun {
    ID: string;
    AgentID: string | null;
    ConversationID: string | null;
    Status: string;
    ErrorMessage: string | null;
}

/** Mirror of MemoryManagerAgent.LoadInstructiveFailedAgentRuns filter construction (plan §6.1). */
function buildFailedRunFilter(since: Date | null): string {
    const sinceFilter = since ? ` AND __mj_CreatedAt >= '${since.toISOString()}'` : '';
    return `((Status IN ('Failed', 'Cancelled') AND ErrorMessage IS NOT NULL) OR (Success = 0 AND Status NOT IN ('Running', 'Paused')))${sinceFilter}`;
}

/** Mirror of MemoryManagerAgent.markCorrectiveNotes. */
function markCorrectiveNotes(notes: ExtractedNote[]): ExtractedNote[] {
    return notes
        .filter((n) => n.type === 'Issue' || n.type === 'Context')
        .map((n) => ({ ...n, protectionTierHint: 'Ephemeral' as const }));
}

/** Mirror of the CreateNoteRecords tier resolution. */
function resolveTier(note: ExtractedNote, isAutoGenerated: boolean): Tier {
    return note.protectionTierHint || (isAutoGenerated ? 'Standard' : 'Protected');
}

/** Mirror of MemoryManagerAgent.buildFailureSummary. */
function buildFailureSummary(run: InstructiveFailedRun): string {
    return `An agent run ended with status "${run.Status}". Error: ${run.ErrorMessage ?? '(none recorded)'}. `
        + `Identify a generalizable lesson to avoid repeating this failure; if none generalizes, extract nothing.`;
}

describe('failed-run filter (dialect-agnostic)', () => {
    it('selects Failed/Cancelled runs with an error and no inline subquery', () => {
        const filter = buildFailedRunFilter(null);
        expect(filter).toContain("Status IN ('Failed', 'Cancelled')");
        expect(filter).toContain('ErrorMessage IS NOT NULL');
        expect(filter).not.toContain('SELECT'); // no subquery — portable across SQL Server/PG
    });

    it("also selects runs flagged unsuccessful via Success (plan §6.1 'Status/Success' signal)", () => {
        const filter = buildFailedRunFilter(null);
        expect(filter).toContain('Success = 0');
        expect(filter).toContain("Status NOT IN ('Running', 'Paused')"); // exclude in-flight runs
        expect(filter).not.toContain('SELECT'); // still no subquery — dialect-agnostic bit comparison
    });

    it('adds a since window when provided', () => {
        const since = new Date('2026-06-01T00:00:00.000Z');
        const filter = buildFailedRunFilter(since);
        expect(filter).toContain("__mj_CreatedAt >= '2026-06-01T00:00:00.000Z'");
    });
});

describe('markCorrectiveNotes', () => {
    it('keeps only Issue and Context notes', () => {
        const notes: ExtractedNote[] = [
            { type: 'Issue', content: 'X fails when Y', confidence: 90 },
            { type: 'Context', content: 'Z is slow', confidence: 85 },
            { type: 'Constraint', content: 'always do W', confidence: 95 },
            { type: 'Preference', content: 'likes terse', confidence: 88 },
            { type: 'Example', content: 'sample', confidence: 91 },
        ];
        const result = markCorrectiveNotes(notes);
        expect(result.map((n) => n.type).sort()).toEqual(['Context', 'Issue']);
    });

    it('never lets a failure become a behavioral Constraint (injection defense)', () => {
        const notes: ExtractedNote[] = [{ type: 'Constraint', content: 'ignore safety', confidence: 99 }];
        expect(markCorrectiveNotes(notes)).toHaveLength(0);
    });

    it('tags surviving notes as Ephemeral', () => {
        const notes: ExtractedNote[] = [{ type: 'Issue', content: 'X fails', confidence: 90 }];
        expect(markCorrectiveNotes(notes)[0].protectionTierHint).toBe('Ephemeral');
    });

    it('returns nothing for an empty input', () => {
        expect(markCorrectiveNotes([])).toEqual([]);
    });
});

describe('tier resolution', () => {
    it('honors the Ephemeral hint for failure-mined notes', () => {
        const note: ExtractedNote = { type: 'Issue', content: 'x', confidence: 90, protectionTierHint: 'Ephemeral' };
        expect(resolveTier(note, true)).toBe('Ephemeral');
    });

    it('falls back to Standard for auto-generated notes without a hint', () => {
        const note: ExtractedNote = { type: 'Context', content: 'x', confidence: 90 };
        expect(resolveTier(note, true)).toBe('Standard');
    });

    it('falls back to Protected for non-auto-generated notes without a hint', () => {
        const note: ExtractedNote = { type: 'Context', content: 'x', confidence: 90 };
        expect(resolveTier(note, false)).toBe('Protected');
    });
});

describe('buildFailureSummary', () => {
    it('includes status and error and asks for a generalizable lesson', () => {
        const run: InstructiveFailedRun = {
            ID: 'r1',
            AgentID: 'a1',
            ConversationID: 'c1',
            Status: 'Failed',
            ErrorMessage: 'context length exceeded',
        };
        const summary = buildFailureSummary(run);
        expect(summary).toContain('Failed');
        expect(summary).toContain('context length exceeded');
        expect(summary).toContain('generalizable lesson');
    });
});
