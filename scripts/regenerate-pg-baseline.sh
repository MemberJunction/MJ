#!/bin/bash
# Regenerate the v5.30 PG baseline from a known-good PG database.
#
# Usage:
#   PGHOST=localhost PGPORT=5432 PGDATABASE=mj_pg_test PGUSER=postgres PGPASSWORD=... \
#     bash scripts/regenerate-pg-baseline.sh
#
# This script dumps the __mj schema + seed metadata (excluding sensitive runtime
# data: AI runs, conversations, audit logs, OAuth tokens, etc.), strips PII from
# the test user row, fixes PG 18 BOOLEAN-default strict-cast issues, and writes
# the result to migrations-pg/v5/B<timestamp>__v5.30__PG_Baseline.pg.sql.
#
# Re-run this whenever the canonical PG schema state advances.

set -euo pipefail

PGDUMP="${PGDUMP:-pg_dump}"
SOURCE_DB="${PGDATABASE:-mj_pg_test}"
OUT_DIR="${OUT_DIR:-migrations-pg/v5}"
# Use a timestamp guaranteed to sort AFTER all v5.30.x V-migrations (latest is
# V202604282300). Adjust if the baseline gets regenerated past v5.31.
BASELINE_TS="${BASELINE_TS:-202604301800}"
BASELINE_VERSION="${BASELINE_VERSION:-v5.30}"
OUT_FILE="${OUT_DIR}/B${BASELINE_TS}__${BASELINE_VERSION}__PG_Baseline.pg.sql"

# Tables whose data is sensitive runtime content and must NOT ship in baseline.
EXCLUDE_TABLES=(
  AIPromptRun AIPromptRunMedia AIAgentRun AIAgentRunStep AIAgentRunMedia
  Conversation ConversationDetail ConversationDetailRating ConversationDetailAttachment
  ConversationDetailArtifact ConversationArtifact ConversationArtifactVersion ConversationArtifactPermission
  AuditLog ActionExecutionLog MCPToolExecutionLog APIKeyUsageLog CommunicationLog CommunicationRun
  ContentItem ContentItemAttribute ContentItemTag ContentItemDuplicate
  ContentProcessRun ContentProcessRunDetail ContentProcessRunPromptRun
  OAuthToken OAuthAuthServerMetadataCache OAuthAuthorizationState OAuthClientRegistration
  RecordChange RecordChangeReplayRun RecordMergeLog RecordMergeDeletionLog
  AIResultCache ScheduledJobRun ErrorLog
  DuplicateRun DuplicateRunDetail DuplicateRunDetailMatch EntityDocumentRun
  UserNotification UserRecordLog
)

# Build pg_dump exclude args
EXCLUDE_ARGS=()
for t in "${EXCLUDE_TABLES[@]}"; do
  EXCLUDE_ARGS+=("--exclude-table-data=${SOURCE_DB%@*}.\"${t}\"")
  EXCLUDE_ARGS+=("--exclude-table-data=__mj.\"${t}\"")
done
# (We pass both forms because pg_dump's pattern matching is precise about
#  the schema-qualifier; in practice only the __mj.* pattern matches.)

TMP_DUMP=$(mktemp)
trap "rm -f $TMP_DUMP" EXIT

echo "Dumping __mj schema from $SOURCE_DB..."
"$PGDUMP" --schema=__mj --no-owner --no-privileges --inserts --column-inserts \
  "${EXCLUDE_ARGS[@]}" > "$TMP_DUMP"

echo "Post-processing into Flyway baseline format..."
mkdir -p "$OUT_DIR"
{
cat <<'HEADER'
-- ============================================================================
-- MemberJunction v5.30 PostgreSQL Baseline
-- Generated by pg_dump from a verified v5.30 mj_pg_test database
-- Regenerate via scripts/regenerate-pg-baseline.sh
-- ============================================================================

-- Extensions
CREATE EXTENSION IF NOT EXISTS "pgcrypto";
CREATE EXTENSION IF NOT EXISTS "uuid-ossp";

-- Schema
CREATE SCHEMA IF NOT EXISTS __mj;
SET search_path TO __mj, public;
SET standard_conforming_strings = on;

-- MJ application roles (must exist before GRANTs in baseline body run)
DO $$ BEGIN
  IF NOT EXISTS (SELECT 1 FROM pg_roles WHERE rolname = 'cdp_UI') THEN
    CREATE ROLE "cdp_UI" NOLOGIN;
  END IF;
  IF NOT EXISTS (SELECT 1 FROM pg_roles WHERE rolname = 'cdp_Developer') THEN
    CREATE ROLE "cdp_Developer" NOLOGIN;
  END IF;
  IF NOT EXISTS (SELECT 1 FROM pg_roles WHERE rolname = 'cdp_Integration') THEN
    CREATE ROLE "cdp_Integration" NOLOGIN;
  END IF;
END $$;

-- ===================== Schema dump (DDL + seed metadata) =====================

HEADER

# Strip:
#   - psql meta-commands (\restrict, \unrestrict — fail outside psql)
#   - pg_dump preamble redundant with our prelude
#   - bare CREATE SCHEMA __mj (we use IF NOT EXISTS in the prelude)
# Scrub:
#   - Test user PII (email/name) — keeps the UUID intact for FK chains
# Fix:
#   - PG 18 strict casts: boolean DEFAULT 0/1 → false/true
sed -E \
    -e '/^[\]restrict /d' \
    -e '/^[\]unrestrict /d' \
    -e '/^SELECT pg_catalog\.set_config.*search_path/d' \
    -e '/^-- Dumped from database/d' \
    -e '/^-- Dumped by pg_dump/d' \
    -e '/^-- PostgreSQL database dump( complete)?$/d' \
    -e '/^CREATE SCHEMA __mj;$/d' \
    -e "s/'josue\.garcia@bluecypress\.io'/'developer.placeholder@nowhere.com'/g" \
    -e "s/'Josue'/'Developer'/g" \
    -e "s/'Garcia'/'Placeholder'/g" \
    -e 's/boolean DEFAULT 0 NOT NULL/boolean DEFAULT false NOT NULL/g' \
    -e 's/boolean DEFAULT 1 NOT NULL/boolean DEFAULT true NOT NULL/g' \
    -e 's/boolean DEFAULT 0,/boolean DEFAULT false,/g' \
    -e 's/boolean DEFAULT 1,/boolean DEFAULT true,/g' \
    "$TMP_DUMP"
} > "$OUT_FILE"

echo "Wrote: $OUT_FILE ($(wc -c < "$OUT_FILE") bytes)"
